def add_devices_in_netbox(nr):

    devices = nr.filter()

    if len(devices.inventory.hosts) == 0:
        raise Exception(f"[{HEADER_GET}] no device selected.")

    data = devices.run(
        task=_generic_add_interface,
        on_failed=True,
        num_workers=10
    )
    print_result(data)

def _generic_add_interface(task):

    data = task.run(
        task=napalm_get,
        getters=["interfaces"]
    )
    interfaces = data.result["interfaces"]
    print(interfaces)